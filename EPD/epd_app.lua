local function build_string_buffer(bytes)
    local chars = {}
    for i = 1, #bytes do
        chars[i] = string.char(bytes[i] or 0xFF)
    end
    return table.concat(chars)
end

function epd.clear()
    local w, h = epd.getSize()
    local buf = string.char(0xFF):rep(math.floor(w * h / 8))
    return drv.display(buf)
end

local function rotate90(buffer)
    local w, h = epd.getSize()
    local rotated = {}
    for i = 1, math.floor(w * h / 8) do
        rotated[i] = 0x00
    end

    for y = 0, h - 1 do
        for x = 0, w - 1 do
            local byteidx = math.floor((y * w + x) / 8) + 1
            local bitidx = 7 - (x % 8)
            local bitval = bit32.band(bit32.rshift(buffer:byte(byteidx), bitidx), 0x01)
            local nx = y
            local ny = w - 1 - x
            local nbyteidx = math.floor((ny * h + nx) / 8) + 1
            local nbitidx = 7 - (nx % 8)
            if bitval == 1 then
                rotated[nbyteidx] = bit32.bor(rotated[nbyteidx], bit32.lshift(1, nbitidx))
            end
        end
    end

    return build_string_buffer(rotated)
end

local function normalize_text(str)
    local text = type(str) == "string" and str or tostring(str or "")
    return text:gsub("\r\n", "\n"):gsub("\r", "\n")
end

local function build_text_buffer(str, opts)
    local w, h = epd.getSize()
    local buf = {}
    for i = 1, math.floor(w * h / 8) do
        buf[i] = 0xFF
    end

    opts = opts or {}
    local marginX = opts.marginX or 2
    local marginY = opts.marginY or 2
    local lineSpacing = opts.lineSpacing or 1
    local scaleX = opts.scaleX or opts.scale or 1
    local scaleY = opts.scaleY or opts.scale or 1

    local charWidth = 8 * scaleX
    local charHeight = 8 * scaleY
    local x0 = marginX
    local y0 = marginY

    for line in (normalize_text(str) .. "\n"):gmatch("(.-)\n") do
        x0 = marginX
        for i = 1, #line do
            local c = line:sub(i, i)
            local font = epd.font8x8[c] or epd.font8x8[" "]
            for row = 0, 7 do
                for col = 0, 7 do
                    local bitval = bit32.band(bit32.rshift(font[row + 1], 7 - col), 0x01)
                    if bitval == 1 then
                        for sx = 0, scaleX - 1 do
                            for sy = 0, scaleY - 1 do
                                local bx = x0 + col * scaleX + sx
                                local by = y0 + row * scaleY + sy
                                if bx < w and by < h then
                                    local idx = math.floor((by * w + bx) / 8) + 1
                                    local bitindex = 7 - (bx % 8)
                                    buf[idx] = bit32.band(buf[idx], bit32.bnot(bit32.lshift(1, bitindex)))
                                end
                            end
                        end
                    end
                end
            end

            x0 = x0 + charWidth
            if x0 + charWidth > w then
                break
            end
        end

        y0 = y0 + charHeight + lineSpacing
        if y0 + charHeight > h then
            break
        end
    end

    return build_string_buffer(buf)
end

local function count_lines(str)
    local lines = 0
    for _ in (normalize_text(str) .. "\n"):gmatch("(.-)\n") do
        lines = lines + 1
    end
    return lines
end

function epd.getTextOptions(str)
    local line_count = count_lines(str)
    if line_count <= 3 then
        return { marginX = 4, marginY = 4, lineSpacing = 2, scaleX = 1, scaleY = 2 }
    elseif line_count <= 4 then
        return { marginX = 3, marginY = 4, lineSpacing = 0, scaleX = 1, scaleY = 2 }
    elseif line_count <= 7 then
        return { marginX = 2, marginY = 2, lineSpacing = 1, scale = 1 }
    elseif line_count <= 10 then
        return { marginX = 0, marginY = 0, lineSpacing = 0, scale = 1 }
    end

    return { marginX = 1, marginY = 1, lineSpacing = 0, scale = 1 }
end

function epd.drawTextEx8x8(str, opts)
    local strbuf = build_text_buffer(str, opts)
    return drv.display(rotate90(strbuf))
end

function epd.updateDisplay(text, opts)
    return epd.drawTextEx8x8(text, opts or epd.getTextOptions(text))
end

return epd
